const rankMap = new Map<string, number>()
let built = false

function buildMap(): void {
  if (built) return
  built = true
  for (let i = 0; i < ORDERED.length; i++) {
    const w = normalizeLemma(ORDERED[i])
    if (!rankMap.has(w)) rankMap.set(w, i + 1)
  }
}

export function normalizeLemma(w: string): string {
  return w
    .normalize('NFD')
    .replace(/\p{M}/gu, '')
    .toLowerCase()
    .replace(/[^a-zà-ÿ]/gi, '')
}

/** Rank 1 = most frequent in bundled list; unknown → high rank. */
export function wordRank(lemma: string): number {
  buildMap()
  const n = normalizeLemma(lemma)
  return rankMap.get(n) ?? 8000
}

export const LEXICON_SIZE = (): number => {
  buildMap()
  return rankMap.size
}
